const dicts: Record<Locale, Dict> = { en, zh, ar };

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  const raw = dicts[locale][key] ?? dicts.en[key] ?? key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? ""));
}

// Resolve a TText against the current locale.
export function resolveTText(locale: Locale, text: TText): string {
  if (typeof text === "string") return text;
  return translate(locale, text.key, text.params);
}
